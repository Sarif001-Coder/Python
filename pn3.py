# there are total 7 arithmetic_operators.

# (+) Addition, (-) subtraction, (*) multiplication
print(8 + 9)  # 17
print(9 - 4)  # 5
print(3 * 4)  # 12
print("\n")
print(2.0 + 5.5)  # 7.5
print(2.0 * 5)  # 10.0
print("\n")

# (/) division = results are always float.
print(10 / 2)
print(20 / 3.0)
print("\n")

# (%) Modulus
print(14 % 4)
print(15.55 % 3)
print(10 % 3.5)
print("\n")

# (**) Exponential
print(2 ** 3)
print(2.75 ** 3)
print(2 ** 2.5)
print("\n")

# (//) Integer/floor division
print(11 // 2)
print(15.5 // 4)
print(9 // 2.2)

